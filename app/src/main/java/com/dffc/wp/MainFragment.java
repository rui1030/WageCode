    public static MainFragment newInstance(String param1, String param2) {
        MainFragment fragment = new MainFragment();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        args.putString(ARG_PARAM2, param2);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {

        View view = inflater.inflate(R.layout.fragment_main, container, false);
        ButterKnife.bind(this, view);
        return view;
    }

    @Override
    public void onViewCreated(View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        fragMainRoot.getSettings().setJavaScriptEnabled(true);
        //设置编码
        fragMainRoot.getSettings().setDefaultTextEncodingName("utf-8");
        //设置本地调用对象及其接口
        fragMainRoot.addJavascriptInterface(new JavaScriptObject(getContext(), fragMainRoot), "android");
        fragMainRoot.setWebChromeClient(new WebChromeClient());
        fragMainRoot.setWebViewClient(new WebViewClient(){
            @TargetApi(Build.VERSION_CODES.LOLLIPOP)
            @Override
            public boolean shouldOverrideUrlLoading(WebView view, WebResourceRequest request) {
                view.loadUrl(request.getUrl().toString());
                return true;
            }

            @Override
            public boolean shouldOverrideUrlLoading(WebView view, String url) {
                view.loadUrl(url);
                return true;
            }
        });
        fragMainRoot.loadUrl("http://192.168.70.104:80/demo.html");
//        fragMainRoot.loadUrl(ConfigUtil.URL);
    }

    // TODO: Rename method, update argument and hook method into UI event
